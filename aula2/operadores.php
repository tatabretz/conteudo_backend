<p> Exemplo </p>

<?php
    /*o operador . serve para concatenar
    juntar dois textos, ou um número e
    um texto*/
    echo 10 + 5 . "<br>";
    echo 10 - 5 . "<br>";
    echo 10 * 5 . "<br>";
    echo 10 / 5 . "<br>";
    echo 10 % 5 . "<br>";
    echo 10 ** 5 . "<br>"; //potência

?>